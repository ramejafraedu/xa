def _is_frame_cache_error(stderr_text: str) -> bool:
    """Detect the specific frame_cache.rs crash in Remotion compositor.

    This error indicates a Rust panic in the frame cache system,
    typically caused by race conditions, corrupted cache, or memory pressure.
    """
    if not stderr_text:
        return False
    indicators = [
        "frame_cache.rs",
        "thread '<unnamed>' panicked",
        "called `Option::unwrap()` on a `None` value",
        "Compositor exited with code 1",
    ]
    lowered = stderr_text.lower()
    return any(ind.lower() in lowered for ind in indicators)


def _clear_remotion_cache() -> None:
    """Clear Remotion's frame and asset caches to recover from corruption.

    Removes:
    - ~/.cache/remotion (global frame cache)
    - remotion-composer/.cache (local bundler cache)
    - remotion-composer/node_modules/.cache
    - Any stale core dumps
    """
    cache_paths = [
        Path.home() / ".cache" / "remotion",
        REMOTION_DIR / ".cache",
        REMOTION_DIR / "node_modules" / ".cache",
    ]

    for cache_path in cache_paths:
        if cache_path.exists():
            try:
                shutil.rmtree(cache_path, ignore_errors=True)
                logger.info(f"🧹 Cleared Remotion cache: {cache_path}")
            except Exception as exc:
                logger.debug(f"Could not clear cache {cache_path}: {exc}")

    # Nota: Ya NO borramos public/workspace aquí para no causar "missing assets after reattempt".
    # La materialización forzada se encarga de sobreescribir lo necesario.

def _force_rebuild_remotion_bundle(timeout: int = 900) -> None:
    """Attempt to rebuild the remotion-composer bundle (npm run build).

    This is invoked on compositor panic detection to ensure the bundle
    is not corrupted and native artifacts are up to date.
    """
    try:
        pkg = REMOTION_DIR / "package.json"
        if not pkg.exists():
            return
        logger.info("🔧 Rebuilding remotion-composer bundle (npm run build)...")
        result = subprocess.run(
            ["npm", "run", "build"],
            cwd=str(REMOTION_DIR),
            capture_output=True,
            text=True,
            timeout=timeout,
            shell=False,
        )
        if result.returncode == 0:
            logger.info("✅ Remotion bundle rebuilt successfully")
        else:
            logger.warning(f"Remotion rebuild failed (rc={result.returncode}): {result.stderr[:1000]}")
    except Exception as exc:
        logger.debug(f"Remotion rebuild error: {exc}")


def _run_remotion_with_recovery(
    props: dict,
    composition_id: str,
    output_path_str: str,
    remotion_runner: list[str],
    remotion_timeout: int,
    max_retries: int = 2,
) -> subprocess.CompletedProcess:
    """Run Remotion with automatic recovery from frame_cache crashes.

    Strategy:
    1. First attempt with normal settings
    2. If frame_cache error detected: clear cache, retry with concurrency=1
    3. If still failing: return last error result
    """
    # Build base command
    def _build_cmd(concurrency: int) -> list[str]:
        payload_json = json.dumps(props, ensure_ascii=False)
        return [
            *remotion_runner,
            "render",
            "src/index.tsx",
            composition_id,
            output_path_str,
            "--props", payload_json,
            "--codec", "h264",
            "--width", "1080",
            "--height", "1920",
            "--concurrency", str(concurrency),
        ]

    env = os.environ.copy()
    env["RUST_BACKTRACE"] = "1"
    # Apply configurable compositor / Node memory limits when set in config
    try:
        mem_mb = int(getattr(settings, "remotion_compositor_memory_limit", 0) or 0)
        if mem_mb > 0:
            # V8 heap limit for Node
            existing = env.get("NODE_OPTIONS", "")
            node_opt = f"--max-old-space-size={mem_mb}"
            if existing:
                env["NODE_OPTIONS"] = existing + " " + node_opt
            else:
                env["NODE_OPTIONS"] = node_opt
            env["REMOTION_COMPOSITOR_MEMORY_LIMIT"] = str(mem_mb)
            logger.info(f"Applied Remotion compositor memory limit: {mem_mb}MB")
    except Exception:
        pass

    last_result = None

    for attempt in range(max_retries + 1):
        # Adjust concurrency: normal on first attempt, reduced on retries
        concurrency = 1 if attempt > 0 else max(1, int(getattr(settings, "remotion_concurrency", 1) or 1))

        if attempt > 0:
            logger.info(f"🔄 Remotion retry attempt {attempt}/{max_retries} (concurrency={concurrency})")

        cmd = _build_cmd(concurrency)

        result = subprocess.run(
            cmd,
            cwd=str(REMOTION_DIR),
            env=env,
            capture_output=True,
            text=True,
            timeout=remotion_timeout,
            shell=False,
        )

        last_result = result

        # Success
        if result.returncode == 0:
            if attempt > 0:
                logger.info(f"✅ Remotion recovered after {attempt} retry(s)")
            return result

        # Check for frame_cache error
        if _is_frame_cache_error(result.stderr or ""):
            logger.warning(
                f"🚨 Detected frame_cache crash (attempt {attempt + 1}). "
                f"Clearing cache and reducing concurrency..."
            )
            _clear_remotion_cache()

            # Try rebuilding the Remotion bundle to avoid corrupted artifacts
            try:
                _force_rebuild_remotion_bundle()
            except Exception:
                pass

            # Force garbage collection of any stale file handles
            try:
                import gc
                gc.collect()
            except Exception:
                pass

            continue  # Retry with reduced settings

        # Other error - don't retry
        break

    return last_result
