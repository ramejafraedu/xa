def _normalize_static_asset_path(value: str) -> str:
    """Return a staticFile-compatible path (workspace relative) when possible."""
    normalized = _to_file_uri(str(value or "").strip())
    if not normalized:
        return ""

    lowered = normalized.lower()
    if lowered.startswith("workspace/"):
        return normalized

    if lowered.startswith("http://") or lowered.startswith("https://") or lowered.startswith("data:"):
        return ""
    if lowered.startswith("file://"):
        return ""

    if normalized.startswith("/") or normalized.startswith("\\") or ":" in normalized:
        return ""

    return normalized.replace("\\", "/")


_NON_IMAGE_ASSET_EXTENSIONS = {
    ".mp4", ".mov", ".m4v", ".webm", ".mkv", ".avi", ".wmv", ".flv", ".mpeg", ".mpg", ".ts", ".m2ts", ".3gp",
    ".mp3", ".wav", ".aac", ".m4a", ".ogg", ".flac", ".opus", ".aif", ".aiff", ".wma",
}


def _is_non_image_asset_path(value: str) -> bool:
    """Return True when a path clearly points to video/audio media."""
    src = str(value or "").strip()
    if not src:
        return False

    lowered = src.lower()
    if lowered.startswith("data:image/"):
        return False

    candidate = src.split("?", 1)[0].split("#", 1)[0].strip()
    if candidate.lower().startswith("file://"):
        candidate = unquote(candidate[7:])

    suffix = Path(candidate).suffix.lower()
    if not suffix:
        return False

    return suffix in _NON_IMAGE_ASSET_EXTENSIONS


