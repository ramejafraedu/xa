def _extract_thumbnail(video_path: Path, temp_dir: Path, timestamp: int) -> Optional[Path]:
    """Extract a thumbnail frame from rendered video."""
    thumb = temp_dir / f"thumb_{timestamp}.jpg"
    try:
        result = subprocess.run(
            [
                "ffmpeg", "-y", "-ss", "00:00:02",
                "-i", str(video_path.as_posix()),
                "-frames:v", "1",
                str(thumb.as_posix()),
            ],
            capture_output=True,
            text=True,
            timeout=60,
        )
        if result.returncode == 0 and thumb.exists() and thumb.stat().st_size > 100:
            return thumb
    except Exception:
        pass
    return None


def _slugify(text: str, max_len: int = 32) -> str:
    """Convert text to filename-safe slug."""
    slug = (text or "").lower()
    slug = re.sub(r"[^a-z0-9]+", "_", slug)
    slug = slug.strip("_")
    return slug[:max_len] or "video"


def _privacy_ffmpeg_args() -> list[str]:
    """Common FFmpeg args to strip container metadata from outputs."""
    return [
        "-map_metadata", "-1",
        "-metadata", "title=",
        "-metadata", "comment=",
        "-metadata", "description=",
        "-metadata", "artist=",
        "-metadata", "copyright=",
        "-metadata", "encoder=",
    ]


def _sanitize_video_metadata(video_path: Path) -> str:
    """Final metadata-strip pass with stream copy to preserve quality."""
    clean_path = video_path.with_name(f"{video_path.stem}_cleanmeta{video_path.suffix}")
    try:
        result = subprocess.run(
            [
                "ffmpeg", "-y", "-threads", "2",
                "-i", str(video_path.as_posix()),
                "-map", "0:v:0", "-map", "0:a?",
                "-c", "copy",
                "-movflags", "+faststart",
                *_privacy_ffmpeg_args(),
                str(clean_path.as_posix()),
            ],
            capture_output=True,
            text=True,
            timeout=120,
        )
        if result.returncode != 0:
            return (result.stderr or "ffmpeg metadata strip failed")[-240:]

        if not clean_path.exists() or clean_path.stat().st_size <= 1024:
            clean_path.unlink(missing_ok=True)
            return "metadata strip produced invalid file"

        video_path.unlink(missing_ok=True)
        clean_path.rename(video_path)
        return ""
    except Exception as exc:
        clean_path.unlink(missing_ok=True)
        return str(exc)


def _build_legacy_props(
    clips: list[Path],
    audio_path: Path,
    subtitles_path: Optional[Path],
    music_path: Optional[Path],
    metadata: dict,
) -> dict:
    """Build baseline CinematicRenderer props when no timeline exists."""
    duration = float(metadata.get("duration", 30.0) or 30.0)
    valid_clips = [p for p in clips if p.exists() and p.stat().st_size > 1000]
    scenes: list[dict] = []

    if valid_clips:
        scene_dur = max(0.8, duration / max(len(valid_clips), 1))
        cursor = 0.0
        for idx, clip in enumerate(valid_clips):
            scenes.append(
                {
                    "id": f"legacy_scene_{idx + 1}",
                    "kind": "video",
                    "src": _to_file_uri(str(clip.resolve().as_posix())),
                    "startSeconds": round(cursor, 3),
                    "durationSeconds": round(scene_dur, 3),
                    "tone": "steel",
                    "fadeInFrames": 6,
                    "fadeOutFrames": 6,
                    "filter": "contrast(1.06) saturate(0.90) brightness(0.98)",
                }
            )
            cursor += scene_dur
    else:
        scenes.append(
            {
                "id": "legacy_title",
                "kind": "title",
                "text": metadata.get("titulo", "Video Factory"),
                "startSeconds": 0.0,
                "durationSeconds": round(max(3.0, duration), 3),
                "accent": "#86d8ff",
                "intensity": 1.0,
            }
        )

    payload: dict = {
        "scenes": scenes,
        "titleFontSize": 72,
        "titleWidth": 860,
        "signalLineCount": 18,
    }

    if audio_path.exists():
        payload["soundtrack"] = {
            "src": _to_file_uri(str(audio_path.resolve().as_posix())),
            "volume": 1.0,
            "fadeInSeconds": 0.15,
            "fadeOutSeconds": 0.20,
        }

    if music_path and music_path.exists():
        payload["music"] = {
            "src": _to_file_uri(str(music_path.resolve().as_posix())),
            "volume": 0.16,
            "fadeInSeconds": 0.5,
            "fadeOutSeconds": 1.2,
        }

    caption_words = _parse_ass_words(subtitles_path)
    if caption_words:
        payload["captions"] = {
            "words": caption_words,
            "wordsPerPage": 4,
            "fontSize": 52,
            "color": "#F8FAFC",
            "highlightColor": "#FBBF24",
            "backgroundColor": "rgba(0, 0, 0, 0.60)",
        }

    payload["audioDurationInSeconds"] = round(float(duration), 3)

    return payload


def _normalize_timeline_props(
    timeline_payload: dict,
    metadata: dict,
    subtitles_path: Optional[Path] = None,
    composition_id: str = DEFAULT_REMOTION_COMPOSITION,
) -> dict:
    """Normalize timeline payload into strict composition-specific props."""
    props = dict(timeline_payload or {})
    if composition_id == "UniversalCommercial":
        return _normalize_universal_commercial_props(props, metadata)

    scenes_raw = props.get("scenes") if isinstance(props.get("scenes"), list) else []
    scenes: list[dict] = []
    for idx, raw in enumerate(scenes_raw):
        if not isinstance(raw, dict):
            continue

        kind = str(raw.get("kind", "video")).strip().lower()
        if kind not in {"video", "image", "title"}:
            kind = "video"
        scene_id = str(raw.get("id") or f"scene_{idx + 1}")
        start_seconds = float(raw.get("startSeconds", 0.0) or 0.0)
        duration_seconds = max(0.8, float(raw.get("durationSeconds", 1.5) or 1.5))

        if kind == "title":
            scenes.append(
                {
                    "id": scene_id,
                    "kind": "title",
                    "text": str(raw.get("text") or metadata.get("titulo") or "Video Factory"),
                    "accent": str(raw.get("accent", "#86d8ff")),
                    "intensity": float(raw.get("intensity", 1.0) or 1.0),
                    "startSeconds": round(start_seconds, 3),
                    "durationSeconds": round(duration_seconds, 3),
                }
            )
            continue

        src = str(raw.get("src", "")).strip()
        if not src:
            continue

        raw_path = src.replace("file://", "").replace("file:///", "").split("?")[0]
        try:
            p = Path(raw_path)
            if p.exists() and p.stat().st_size < 100:
                logger.warning(f"Discarding empty media file: {src}")
                continue
        except Exception:
            pass

        scene: dict = {
            "id": scene_id,
            "kind": kind,
            "src": _to_file_uri(src),
            "startSeconds": round(start_seconds, 3),
            "durationSeconds": round(duration_seconds, 3),
            "tone": str(raw.get("tone", "steel")),
            "fadeInFrames": int(raw.get("fadeInFrames", 6) or 0),
            "fadeOutFrames": int(raw.get("fadeOutFrames", 6) or 0),
            "filter": str(raw.get("filter", "contrast(1.06) saturate(0.90) brightness(0.98)")),
        }

        if kind == "image" and _is_non_image_asset_path(str(scene.get("src") or "")):
            # Defensive normalization: malformed timelines may tag video files as image scenes.
            scene["kind"] = "video"

        if scene.get("kind") == "image":
            if "animation" in raw:
                scene["animation"] = str(raw["animation"])
            if "animationIntensity" in raw:
                scene["animationIntensity"] = float(raw["animationIntensity"])
            if "animationDirection" in raw:
                scene["animationDirection"] = str(raw["animationDirection"])
            if "overlayParticles" in raw:
                scene["overlayParticles"] = bool(raw["overlayParticles"])

        if "trimBeforeSeconds" in raw:
            scene["trimBeforeSeconds"] = float(raw.get("trimBeforeSeconds") or 0.0)
        if "trimAfterSeconds" in raw:
            scene["trimAfterSeconds"] = float(raw.get("trimAfterSeconds") or 0.0)
        scenes.append(scene)

    if not scenes:
        scenes = [
            {
                "id": "timeline_fallback",
                "kind": "title",
                "text": str(metadata.get("titulo") or "Video Factory"),
                "startSeconds": 0.0,
                "durationSeconds": round(max(3.0, float(metadata.get("duration", 10.0) or 10.0)), 3),
                "accent": "#86d8ff",
                "intensity": 1.0,
            }
        ]

    props["scenes"] = scenes
    props["titleFontSize"] = int(props.get("titleFontSize", 72) or 72)
    props["titleWidth"] = int(props.get("titleWidth", 860) or 860)
    props["signalLineCount"] = int(props.get("signalLineCount", 18) or 18)

    soundtrack = props.get("soundtrack")
    if isinstance(soundtrack, dict) and soundtrack.get("src"):
        props["soundtrack"] = {
            **soundtrack,
            "src": _to_file_uri(str(soundtrack.get("src"))),
        }
    else:
        props.pop("soundtrack", None)

    music = props.get("music")
    if isinstance(music, dict) and music.get("src"):
        props["music"] = {
            **music,
            "src": _to_file_uri(str(music.get("src"))),
        }
    else:
        props.pop("music", None)

    captions = props.get("captions") if isinstance(props.get("captions"), dict) else {}
    normalized_words: list[dict] = []

    if isinstance(captions.get("words"), list):
        for w in captions.get("words", []):
            if not isinstance(w, dict):
                continue
            word = str(w.get("word", "")).strip()
            if not word:
                continue
            start_ms = int(w.get("startMs", 0) or 0)
            end_ms = int(w.get("endMs", start_ms + 40) or (start_ms + 40))
            if end_ms <= start_ms:
                end_ms = start_ms + 40
            normalized_words.append({"word": word, "startMs": start_ms, "endMs": end_ms})

    if not normalized_words and subtitles_path and subtitles_path.exists():
        normalized_words = _parse_ass_words(subtitles_path)
        if normalized_words:
            logger.info("Remotion captions fallback applied from ASS subtitles")

    if normalized_words:
        props["captions"] = {
            **captions,
            "words": normalized_words,
            "wordsPerPage": int(captions.get("wordsPerPage", 4) or 4),
            "fontSize": int(captions.get("fontSize", 52) or 52),
            "color": str(captions.get("color", "#F8FAFC")),
            "highlightColor": str(captions.get("highlightColor", "#FBBF24")),
            "backgroundColor": str(captions.get("backgroundColor", "rgba(0, 0, 0, 0.60)")),
        }
    else:
        props.pop("captions", None)

    props["audioDurationInSeconds"] = round(float(metadata.get("duration", 0.0) or 0.0), 3)

    return props


def build_director_artifacts(
    timeline_payload: dict,
    metadata: dict,
    artifacts_dir: Path,
    *,
    subtitles_path: Optional[Path] = None,
) -> tuple[Path, Path, dict, dict]:
    """Build, validate, and persist director artifacts for auditability.

    Returns:
        (director_json_path, director_meta_path, director_payload, director_meta)
    """
    payload = dict(timeline_payload or {})
    composition_id = _resolve_composition_id(metadata, payload)
    normalized_props = _normalize_timeline_props(
        payload,
        metadata,
        subtitles_path=subtitles_path,
        composition_id=composition_id,
    )

    try:
        timestamp = int(metadata.get("timestamp") or int(time.time() * 1000))
    except (TypeError, ValueError, AttributeError):
        timestamp = int(time.time() * 1000)

    director_payload = {
        "version": "1.0",
        "composition_id": composition_id,
        "timestamp": timestamp,
        "job_id": str(metadata.get("job_id") or ""),
        "props": normalized_props,
    }

    props_hash = hashlib.sha1(
        json.dumps(normalized_props, ensure_ascii=False, sort_keys=True).encode("utf-8")
    ).hexdigest()
    director_meta = {
        "version": "1.0",
        "composition_id": composition_id,
        "timestamp": timestamp,
        "job_id": str(metadata.get("job_id") or ""),
        "props_hash": props_hash,
        "render_item_count": int(_render_item_count(normalized_props, composition_id)),
        "asset_reference_count": int(_count_asset_references(normalized_props, composition_id)),
        "has_captions": bool(normalized_props.get("captions")),
        "has_soundtrack": bool(isinstance(normalized_props.get("soundtrack"), dict)),
        "has_music": bool(isinstance(normalized_props.get("music"), dict)),
    }

    from schemas.artifacts import validate_artifact

    validate_artifact("director", director_payload)
    validate_artifact("director_meta", director_meta)

    artifacts_dir.mkdir(parents=True, exist_ok=True)
    director_path = artifacts_dir / f"director_{timestamp}.json"
    director_meta_path = artifacts_dir / f"director_meta_{timestamp}.json"

    director_path.write_text(
        json.dumps(director_payload, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    director_meta_path.write_text(
        json.dumps(director_meta, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )

    return director_path, director_meta_path, director_payload, director_meta


def build_edit_decisions_artifact(
    timeline_payload: dict,
    metadata: dict,
    artifacts_dir: Path,
    *,
    subtitles_path: Optional[Path] = None,
    narration_audio_path: Optional[Path] = None,
    music_path: Optional[Path] = None,
    incremental_eml_seed: Optional[dict] = None,
) -> tuple[Path, dict]:
    """Build, validate, and persist an OpenMontage-compatible edit_decisions artifact."""
    payload = dict(timeline_payload or {})
    composition_id = _resolve_composition_id(metadata, payload)
    normalized_props = _normalize_timeline_props(
        payload,
        metadata,
        subtitles_path=subtitles_path,
        composition_id=composition_id,
    )

    try:
        timestamp = int(metadata.get("timestamp") or int(time.time() * 1000))
    except (TypeError, ValueError, AttributeError):
        timestamp = int(time.time() * 1000)

    cuts = _build_edit_decision_cuts(normalized_props, composition_id)
    mapper_name = "timeline"
    mapper_fallback = False

    if not cuts:
        seed_cuts = _extract_incremental_seed_cuts(incremental_eml_seed)
        if seed_cuts:
            cuts = seed_cuts
            mapper_name = "editor_incremental"
            mapper_fallback = True

    seed_cut_count = 0
    if isinstance(incremental_eml_seed, dict) and isinstance(incremental_eml_seed.get("cuts"), list):
        seed_cut_count = len(incremental_eml_seed.get("cuts") or [])

    edit_decisions: dict = {
        "version": "1.0",
        "cuts": cuts,
        "renderer_family": "animation-first",
        "metadata": {
            "composition_id": composition_id,
            "timestamp": timestamp,
            "job_id": str(metadata.get("job_id") or ""),
            "generated_by": "renderer_remotion.build_edit_decisions_artifact",
            "render_item_count": int(_render_item_count(normalized_props, composition_id)),
            "asset_reference_count": int(_count_asset_references(normalized_props, composition_id)),
            "cut_mapper": mapper_name,
            "cut_mapper_fallback": mapper_fallback,
            "incremental_seed_cut_count": int(seed_cut_count),
        },
    }

    subtitles_payload = _build_edit_decisions_subtitles(
        subtitles_path=subtitles_path,
        normalized_props=normalized_props,
    )
    if subtitles_payload:
        edit_decisions["subtitles"] = subtitles_payload

    audio_payload, legacy_music = _build_edit_decisions_audio(
        normalized_props=normalized_props,
        composition_id=composition_id,
        narration_audio_path=narration_audio_path,
        music_path=music_path,
    )
    if audio_payload:
        edit_decisions["audio"] = audio_payload
    if legacy_music:
        edit_decisions["music"] = legacy_music

    from schemas.artifacts import validate_artifact

    validate_artifact("edit_decisions", edit_decisions)

    artifacts_dir.mkdir(parents=True, exist_ok=True)
    edit_decisions_path = artifacts_dir / f"edit_decisions_{timestamp}.json"
    edit_decisions_path.write_text(
        json.dumps(edit_decisions, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    return edit_decisions_path, edit_decisions


def _build_edit_decision_cuts(normalized_props: dict, composition_id: str) -> list[dict]:
    """Map composition-specific props to edit_decisions.cuts format.

    V16 PRO: hook feature is capped at ``short_hook_max_seconds`` and
    subsequent features fall inside ``[short_scene_min_seconds,
    short_scene_max_seconds]``. Transition duration is
    ``short_transition_seconds`` (default 0.08s) for punchy cuts.
    """
    hard_limit = bool(getattr(settings, "enforce_duration_hard_limit", False))
    scene_min = float(getattr(settings, "short_scene_min_seconds", 2.5))
    scene_max = float(getattr(settings, "short_scene_max_seconds", 5.0))
    hook_max = float(getattr(settings, "short_hook_max_seconds", 2.0))
    max_scenes = int(getattr(settings, "short_max_scenes", 12))
    transition_dur = float(getattr(settings, "short_transition_seconds", 0.08))

    if composition_id == "UniversalCommercial":
        script = normalized_props.get("script") if isinstance(normalized_props.get("script"), dict) else {}
        features = script.get("features") if isinstance(script.get("features"), list) else []
        cuts: list[dict] = []
        feature_iter = features[:max_scenes] if hard_limit else features
        total_features = len(feature_iter)
        for idx, feature in enumerate(feature_iter):
            if not isinstance(feature, dict):
                continue
            image_path = str(feature.get("imagePath") or "").strip()
            if not image_path:
                continue

            if hard_limit:
                duration = hook_max if idx == 0 else min(scene_max, max(scene_min, 4.0))
                td = transition_dur
            else:
                duration = 6.0
                td = 0.15
            title = str(feature.get("title") or f"Feature {idx + 1}")
            # Pick animation: hook = punch-in, last = zoom-out (micro-loop), rest = slow-zoom.
            if idx == 0 and hard_limit:
                anim = "punch-in-fast"
            elif idx >= total_features - 1 and total_features > 1:
                anim = "ken-burns-zoom-out"
            else:
                anim = "ken-burns-slow-zoom"
            # Rotate transition only in hard-limit short-form mode; keep legacy "cut" otherwise.
            transition_out = _dynamic_transition_for_index(idx, total_features) if hard_limit else "cut"
            cuts.append(
                {
                    "id": f"feature_{idx + 1}",
                    "source": _to_file_uri(image_path),
                    "in_seconds": 0.0,
                    "out_seconds": duration,
                    "layer": "primary",
                    "transform": {
                        "scale": 1.0,
                        "position": "center",
                        "animation": anim,
                    },
                    "transition_in": "cut",
                    "transition_out": transition_out,
                    "transition_duration": td,
                    "reason": f"Feature visual: {title[:120]}",
                }
            )
        return cuts

    scenes = normalized_props.get("scenes") if isinstance(normalized_props.get("scenes"), list) else []
    if hard_limit and len(scenes) > max_scenes:
        scenes = scenes[:max_scenes]
    total_scenes = len(scenes)
    cuts = []
    for idx, scene in enumerate(scenes):
        if not isinstance(scene, dict):
            continue

        src = str(scene.get("src") or "").strip()
        if not src:
            continue

        try:
            duration = max(0.8, float(scene.get("durationSeconds", 0.0) or 0.0))
        except (TypeError, ValueError):
            duration = 0.8

        if hard_limit:
            if idx == 0:
                duration = min(duration, hook_max)
            else:
                duration = max(scene_min, min(scene_max, duration))

        try:
            speed = max(0.1, float(scene.get("speedFactor", 1.0) or 1.0))
        except (TypeError, ValueError):
            speed = 1.0

        zoom_animation = _zoom_to_transform_animation(str(scene.get("zoomType") or ""))
        transform: dict = {
            "scale": 1.0,
            "position": "center",
        }
        if zoom_animation:
            transform["animation"] = zoom_animation
        elif hard_limit:
            # V16.1: hook = punch-in, last = zoom-out (micro-loop), rest = slow-zoom.
            if idx == 0:
                transform["animation"] = "punch-in-fast"
            elif idx >= total_scenes - 1 and total_scenes > 1:
                transform["animation"] = "ken-burns-zoom-out"
            else:
                transform["animation"] = "ken-burns-slow-zoom"

        try:
            fade_out_frames = int(scene.get("fadeOutFrames", 0) or 0)
        except (TypeError, ValueError):
            fade_out_frames = 0

        computed_transition = round(max(0.0, fade_out_frames / 30.0), 3)
        if hard_limit:
            # Cap transition duration so cuts stay snappy for shorts.
            computed_transition = min(computed_transition, transition_dur)

        # V16.1: dynamic transition rotation when the scene doesn't specify one.
        scene_transition = str(scene.get("transitionOut") or "").strip()
        if not scene_transition:
            if hard_limit:
                scene_transition = _dynamic_transition_for_index(idx, total_scenes)
            else:
                scene_transition = "cut"

        cut = {
            "id": str(scene.get("id") or f"cut_{idx + 1}"),
            "source": _to_file_uri(src),
            "in_seconds": 0.0,
            "out_seconds": round(duration, 3),
            "speed": speed,
            "layer": "primary",
            "transform": transform,
            "transition_in": "cut",
            "transition_out": _normalize_cut_transition(scene_transition),
            "transition_duration": computed_transition,
        }

        reason = str(scene.get("sceneText") or "").strip()
        if reason:
            cut["reason"] = reason[:180]

        cuts.append(cut)

    return cuts


def _extract_incremental_seed_cuts(incremental_eml_seed: Optional[dict]) -> list[dict]:
    """Extract schema-safe cuts from editor incremental EML seed payload."""
    if not isinstance(incremental_eml_seed, dict):
        return []

    raw_cuts = incremental_eml_seed.get("cuts")
    if not isinstance(raw_cuts, list):
        return []

    cuts: list[dict] = []
    for idx, item in enumerate(raw_cuts):
        if not isinstance(item, dict):
            continue

        source = str(item.get("source") or "").strip()
        if not source:
            continue

        try:
            in_seconds = max(0.0, float(item.get("in_seconds", 0.0) or 0.0))
        except (TypeError, ValueError):
            in_seconds = 0.0

        try:
            out_seconds = max(0.0, float(item.get("out_seconds", 0.0) or 0.0))
        except (TypeError, ValueError):
            out_seconds = 0.0

        if out_seconds <= in_seconds:
            out_seconds = round(in_seconds + 0.8, 3)

        cut: dict = {
            "id": str(item.get("id") or f"seed_cut_{idx + 1}"),
            "source": _to_file_uri(source),
            "in_seconds": round(in_seconds, 3),
            "out_seconds": round(out_seconds, 3),
        }

        try:
            speed = max(0.1, float(item.get("speed", 1.0) or 1.0))
            cut["speed"] = speed
        except (TypeError, ValueError):
            pass

        layer = str(item.get("layer") or "").strip().lower()
        if layer in {"primary", "overlay", "background"}:
            cut["layer"] = layer

        transform = item.get("transform") if isinstance(item.get("transform"), dict) else None
        if transform:
            clean_transform: dict = {}
            if "scale" in transform:
                try:
                    clean_transform["scale"] = max(0.0, float(transform.get("scale") or 0.0))
                except (TypeError, ValueError):
                    pass
            if "position" in transform:
                clean_transform["position"] = str(transform.get("position") or "center")
            if "animation" in transform:
                clean_transform["animation"] = str(transform.get("animation") or "")
            if clean_transform:
                cut["transform"] = clean_transform

        transition_in = str(item.get("transition_in") or "").strip()
        if transition_in:
            cut["transition_in"] = transition_in
        transition_out = str(item.get("transition_out") or "").strip()
        if transition_out:
            cut["transition_out"] = transition_out

        if "transition_duration" in item:
            try:
                cut["transition_duration"] = max(0.0, float(item.get("transition_duration") or 0.0))
            except (TypeError, ValueError):
                pass

        reason = str(item.get("reason") or "").strip()
        if reason:
            cut["reason"] = reason[:180]

        cuts.append(cut)

    return cuts


