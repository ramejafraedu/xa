def _build_universal_features_from_scenes(scenes: list[dict]) -> list[dict]:
    """Approximate UniversalCommercial features from generic scene entries."""
    features: list[dict] = []
    for idx, scene in enumerate(scenes):
        if not isinstance(scene, dict):
            continue

        title = str(scene.get("title") or scene.get("text") or scene.get("id") or "").strip()
        subtitle = str(scene.get("subtitle") or scene.get("sceneText") or "").strip()
        scene_kind = str(scene.get("kind") or "").strip().lower()
        image_candidate = str(scene.get("imagePath") or "").strip()
        if not image_candidate and scene_kind in {"image", "photo", "still"}:
            image_candidate = str(scene.get("src") or "")
        if not image_candidate and not scene_kind:
            image_candidate = str(scene.get("src") or "")

        image_path = _normalize_static_asset_path(image_candidate)
        if image_path and _is_non_image_asset_path(image_path):
            image_path = ""

        if not title:
            title = f"Feature {idx + 1}"
        if not subtitle:
            subtitle = "Beneficio principal"

        entry = {
            "title": title,
            "subtitle": subtitle,
        }
        if image_path:
            entry["imagePath"] = image_path
        features.append(entry)

        if len(features) >= 4:
            break

    return features


def _to_file_uri(value: str) -> str:
    """Normalize asset paths for CinematicRenderer staticFile()/URL handling.

    - HTTP(S)/data/file URIs pass-through.
    - Local files under workspace become "workspace/..." static paths.
    - Other local paths stay absolute (best-effort fallback).
    """
    src = (value or "").strip()
    if not src:
        return src

    lowered = src.lower()
    if lowered.startswith("http://") or lowered.startswith("https://") or lowered.startswith("data:"):
        return src
    if lowered.startswith("file://"):
        return src

    try:
        path = Path(src).resolve()
        workspace_dir = settings.workspace.resolve()
        if path.is_relative_to(workspace_dir):
            rel = path.relative_to(workspace_dir)
            return f"workspace/{rel.as_posix()}"
        return str(path)
    except Exception:
        return src


def _canonical_asset_reference(value: str) -> str:
    """Normalize an asset reference to compare across URL/static/file forms."""
    raw = str(value or "").strip()
    if not raw:
        return ""

    raw = unquote(raw)
    lowered = raw.lower()
    if lowered.startswith("http://") or lowered.startswith("https://"):
        marker = "/public/"
        idx = lowered.find(marker)
        if idx >= 0:
            raw = raw[idx + len(marker):]
    elif lowered.startswith("file://"):
        raw = raw[7:]

    raw = raw.split("?", 1)[0].split("#", 1)[0]
    return raw.replace("\\", "/").lstrip("/")


def _extract_failed_image_sources(stderr_text: str) -> set[str]:
    """Extract image URLs Remotion failed to decode from stderr."""
    if not stderr_text:
        return set()

    sources: set[str] = set()
    matches = re.findall(r"Could not load image with source ([^,\s]+)", stderr_text, flags=re.IGNORECASE)
    for source in matches:
        normalized = _canonical_asset_reference(source)
        if normalized:
            sources.add(normalized)
    return sources


def _sanitize_non_image_sources_in_props(
    props: dict,
    composition_id: str,
    blocked_sources: Optional[set[str]] = None,
) -> int:
    """Remove/convert image-like props that actually point to video/audio assets."""
    blocked = {
        _canonical_asset_reference(item)
        for item in (blocked_sources or set())
        if str(item or "").strip()
    }

    def _must_strip(source_value: str) -> bool:
        normalized = _canonical_asset_reference(source_value)
        if not normalized:
            return False
        if blocked and normalized in blocked:
            return True
        return _is_non_image_asset_path(normalized)

    sanitized = 0

    if composition_id == "UniversalCommercial":
        script = props.get("script") if isinstance(props.get("script"), dict) else {}
        features = script.get("features") if isinstance(script.get("features"), list) else []
        for feature in features:
            if not isinstance(feature, dict):
                continue
            image_path = str(feature.get("imagePath") or "").strip()
            if image_path and _must_strip(image_path):
                feature.pop("imagePath", None)
                sanitized += 1
        return sanitized

    scenes = props.get("scenes") if isinstance(props.get("scenes"), list) else []
    for scene in scenes:
        if not isinstance(scene, dict):
            continue
        if str(scene.get("kind") or "").strip().lower() != "image":
            continue

        src = str(scene.get("src") or "").strip()
        if src and _must_strip(src):
            scene["kind"] = "video"
            scene.pop("animation", None)
            scene.pop("animationIntensity", None)
            scene.pop("animationDirection", None)
            scene.pop("overlayParticles", None)
            sanitized += 1

    return sanitized


def _materialize_workspace_assets(props: dict) -> None:
    """Copy workspace-referenced assets into remotion public/workspace.

    This avoids relying on symlink behavior in bundled render temp dirs.
    """
    public_workspace = REMOTION_DIR / "public" / "workspace"
    public_workspace.mkdir(parents=True, exist_ok=True)
    workspace_dir = settings.workspace.resolve()

    def _source_and_static(src: str) -> tuple[Optional[Path], str]:
        raw = (src or "").strip()
        if not raw:
            return None, raw

        lowered = raw.lower()
        if lowered.startswith("http://") or lowered.startswith("https://") or lowered.startswith("data:"):
            return None, raw

        if lowered.startswith("workspace/"):
            local = settings.workspace.parent / raw
            return local, raw.replace("\\", "/")

        if lowered.startswith("file://"):
            decoded = unquote(raw[7:])
            local = Path(decoded)
        else:
            local = Path(raw)

        try:
            local = local.resolve()
            if local.is_relative_to(workspace_dir):
                rel = local.relative_to(workspace_dir)
                return local, f"workspace/{rel.as_posix()}"
        except Exception:
            return None, raw

        return None, raw

    def _materialize(entry: dict, key: str = "src") -> None:
        if not isinstance(entry, dict):
            return
        src = str(entry.get(key, "") or "")
        local_path, static_src = _source_and_static(src)
        if local_path and local_path.exists() and local_path.is_file():
            rel = static_src.replace("workspace/", "", 1)
            dest = public_workspace / rel
            dest.parent.mkdir(parents=True, exist_ok=True)
            # When public/workspace is a symlink to workspace, source and dest can be identical.
            try:
                if dest.exists() and dest.resolve() == local_path.resolve():
                    entry[key] = static_src
                    return
            except Exception:
                pass

            try:
                shutil.copy2(local_path, dest)
                try:
                    # Ensure data landed on disk to avoid bundler race conditions.
                    with open(dest, "rb") as _f:
                        os.fsync(_f.fileno())
                except Exception:
                    pass
            except shutil.SameFileError:
                # Safe no-op: file already available at target path.
                pass
            entry[key] = static_src

    for scene in props.get("scenes", []):
        if isinstance(scene, dict):
            _materialize(scene, "src")

    # Final sync to reduce chance of missing files when Remotion's bundler
    # snapshots `public` into a temporary bundle directory.
    try:
        os.sync()
    except Exception:
        pass

    soundtrack = props.get("soundtrack")
    if isinstance(soundtrack, dict):
        _materialize(soundtrack, "src")

    music = props.get("music")
    if isinstance(music, dict):
        _materialize(music, "src")

    audio = props.get("audio")
    if isinstance(audio, dict):
        _materialize(audio, "bgmPath")

    script = props.get("script")
    if isinstance(script, dict) and isinstance(script.get("features"), list):
        for feature in script.get("features", []):
            if isinstance(feature, dict):
                _materialize(feature, "imagePath")


def _parse_ass_words(subtitles_path: Optional[Path]) -> list[dict]:
    """Parse ASS subtitles into word-level captions for Remotion overlay."""
    if not subtitles_path or not subtitles_path.exists():
        return []

    words: list[dict] = []
    try:
        lines = subtitles_path.read_text(encoding="utf-8", errors="ignore").splitlines()
        for line in lines:
            if not line.startswith("Dialogue:"):
                continue

            parts = line.split(",", 9)
            if len(parts) < 10:
                continue

            start_s = _ass_time_to_seconds(parts[1])
            end_s = _ass_time_to_seconds(parts[2])
            if end_s <= start_s:
                continue

            raw_text = parts[9]
            if not _append_karaoke_words(words, raw_text, start_s, end_s):
                _append_uniform_words(words, raw_text, start_s, end_s)
    except Exception as exc:
        logger.debug(f"ASS parsing failed for Remotion captions: {exc}")

    return words


def _append_karaoke_words(words: list[dict], raw_text: str, start_s: float, end_s: float) -> bool:
    """Extract precise timings from ASS karaoke tags (\\k/\\kf/\\ko)."""
    karaoke_re = re.compile(r"\{\\(?:k|K|kf|KF|ko|KO)(\d+)\}")
    matches = list(karaoke_re.finditer(raw_text or ""))
    if not matches:
        return False

    chunks: list[tuple[float, list[str]]] = []
    for idx, match in enumerate(matches):
        next_pos = matches[idx + 1].start() if idx + 1 < len(matches) else len(raw_text)
        chunk_text = raw_text[match.end() : next_pos]
        chunk_text = re.sub(r"\{[^}]*\}", "", chunk_text)
        chunk_text = chunk_text.replace("\\N", " ").replace("\\n", " ")
        chunk_text = re.sub(r"\s+", " ", chunk_text).strip()
        tokens = _tokenize_caption_words(chunk_text)
        if not tokens:
            continue

        dur_cs = max(1, int(match.group(1) or 0))
        chunks.append((dur_cs / 100.0, tokens))

    if not chunks:
        return False

    total_tag_span = sum(span for span, _ in chunks)
    line_span = max(0.04, end_s - start_s)
    scale = (line_span / total_tag_span) if total_tag_span > 0 else 1.0

    cursor = start_s
    line_words: list[dict] = []
    for chunk_span, tokens in chunks:
        alloc = max(0.04, chunk_span * scale)
        step = alloc / max(len(tokens), 1)
        for token in tokens:
            w_start = cursor
            w_end = min(end_s, cursor + step)
            line_words.append(
                {
                    "word": token,
                    "startMs": int(round(w_start * 1000)),
                    "endMs": int(round(w_end * 1000)),
                }
            )
            cursor = w_end

    if not line_words:
        return False

    last_end = int(round(end_s * 1000))
    if line_words[-1]["endMs"] < last_end:
        line_words[-1]["endMs"] = last_end

    for item in line_words:
        if item["endMs"] <= item["startMs"]:
            item["endMs"] = item["startMs"] + 40
        words.append(item)
    return True


def _append_uniform_words(words: list[dict], raw_text: str, start_s: float, end_s: float) -> None:
    """Fallback parser for ASS lines without karaoke tags."""
    text = re.sub(r"\{[^}]*\}", "", raw_text or "")
    text = text.replace("\\N", " ").replace("\\n", " ")
    text = re.sub(r"\s+", " ", text).strip()
    tokens = _tokenize_caption_words(text)
    if not tokens:
        return

    span = max(0.04, end_s - start_s)
    step = span / len(tokens)
    cursor = start_s
    for token in tokens:
        start_ms = int(round(cursor * 1000))
        end_ms = int(round(min(end_s, cursor + step) * 1000))
        if end_ms <= start_ms:
            end_ms = start_ms + 40
        words.append({"word": token, "startMs": start_ms, "endMs": end_ms})
        cursor += step


def _tokenize_caption_words(text: str) -> list[str]:
    return re.findall(r"[A-Za-z0-9ÁÉÍÓÚáéíóúÑñÜü]+|[^\s]", text or "")


def _ass_time_to_seconds(value: str) -> float:
    """Convert ASS timestamp (H:MM:SS.CS) to seconds."""
    try:
        hms, centis = value.strip().split(".")
        hh, mm, ss = hms.split(":")
        return int(hh) * 3600 + int(mm) * 60 + int(ss) + (int(centis) / 100.0)
    except Exception:
        return 0.0


def _remotion_output_quality_issue(video_path: Path) -> str:
    """Return a short reason when rendered output is clearly broken.

    This is intentionally lightweight and conservative so Remotion keeps being
    used when healthy, while obviously bad outputs trigger FFmpeg fallback.
    """
    try:
        probe = subprocess.run(
            [
                "ffprobe", "-v", "quiet",
                "-print_format", "json",
                "-show_format", "-show_streams",
                str(video_path),
            ],
            capture_output=True,
            text=True,
            timeout=15,
        )
        if probe.returncode != 0:
            return "ffprobe failed"

        data = json.loads(probe.stdout or "{}")
        streams = data.get("streams", []) if isinstance(data, dict) else []
        video_dur = None
        audio_dur = None
        has_audio_stream = False

        for stream in streams:
            if not isinstance(stream, dict):
                continue
            ctype = stream.get("codec_type")
            dur = stream.get("duration")
            if ctype == "audio":
                has_audio_stream = True
            if dur is None:
                continue
            try:
                dur_f = float(dur)
            except Exception:
                continue
            if ctype == "video" and video_dur is None:
                video_dur = dur_f
            elif ctype == "audio" and audio_dur is None:
                audio_dur = dur_f

        if not has_audio_stream:
            return "missing audio stream"

        if video_dur and audio_dur and abs(video_dur - audio_dur) > 0.8:
            return f"av_desync video={video_dur:.2f}s audio={audio_dur:.2f}s"

        total_duration = None
        try:
            total_duration = float((data.get("format") or {}).get("duration", 0) or 0)
        except Exception:
            total_duration = None

        if total_duration and total_duration >= 5:
            silence = subprocess.run(
                [
                    "ffmpeg", "-i", str(video_path),
                    "-af", "silencedetect=noise=-40dB:d=2",
                    "-f", "null", "-",
                ],
                capture_output=True,
                text=True,
                timeout=35,
            )
            stderr = silence.stderr or ""
            durations = re.findall(r"silence_duration: ([\d.]+)", stderr)
            total_silence = sum(float(d) for d in durations)
            if total_silence > total_duration * 0.3:
                return (
                    f"excessive_silence {total_silence:.1f}s/"
                    f"{total_duration:.1f}s"
                )

    except Exception as exc:
        logger.debug(f"Remotion output sanity check skipped: {exc}")

    return ""


def setup_remotion_project() -> bool:
    """Initialize the Remotion project if not already set up."""
    if REMOTION_DIR.exists() and (REMOTION_DIR / "node_modules").exists():
        logger.debug("Remotion project already initialized")
        return True

    try:
        logger.info("🔧 Setting up Remotion project...")
        REMOTION_DIR.mkdir(parents=True, exist_ok=True)
        result = subprocess.run(
            ["npx", "-y", "create-video@latest", str(REMOTION_DIR), "--template", "blank"],
            capture_output=True,
            text=True,
            timeout=120,
        )
        if result.returncode == 0:
            logger.info("✅ Remotion project initialized")
            return True

        logger.warning(f"Remotion setup failed: {result.stderr[:300]}")
        return False
    except Exception as exc:
        logger.error(f"Remotion setup error: {exc}")
        return False


